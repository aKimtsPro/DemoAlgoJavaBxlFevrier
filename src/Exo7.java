public class Exo7 {

    public static void main(String[] args) {

        for (int i = 1; i < 11; i++) {
            System.out.println(2 +" * "+ i + " = "+ (2*i));
        }

        int cpt = 1;
        while(cpt < 11){
            System.out.println(2 +" * "+ cpt + " = "+ (2*cpt));
            cpt++;
        }

    }
}

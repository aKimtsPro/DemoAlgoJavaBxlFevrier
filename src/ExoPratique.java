public class ExoPratique {

    public static void main(String[] args) {

        int a, b, c, d;
        double e, f;
        a = 10;
        b = 5 / 2;
        c = b + a;
        d = 5 % 2;
        e = 5 / 2.0;
        f = (e - 0.5) * a;

        System.out.println(a); // 10
        System.out.println(b); // 2
        System.out.println(c); // 12
        System.out.println(d); // 1
        System.out.println(e); // 2,5
        System.out.println(f); // 20

    }
}
